package beans;

public class Iphones {
	private String Iid;
    private String Iname;
    private float Iprice;
 
    private String Ibrand;
    private String Icell;
    private String Itime;
	
	
	public String getIname() {
		return Iname;
	}
	public void setIname(String iname) {
		Iname = iname;
	}
	public float getIprice() {
		return Iprice;
	}
	public void setIprice(float iprice) {
		Iprice = iprice;
	}
	
	public String getIbrand() {
		return Ibrand;
	}
	public void setIbrand(String ibrand) {
		Ibrand = ibrand;
	}
	public String getIcell() {
		return Icell;
	}
	public void setIcell(String icell) {
		Icell = icell;
	}
	public String getItime() {
		return Itime;
	}
	public void setItime(String itime) {
		Itime = itime;
	}
	public String getIid() {
		return Iid;
	}
	public void setIid(String iid) {
		Iid = iid;
	}
}
